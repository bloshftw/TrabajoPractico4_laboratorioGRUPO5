    /*GUARDAR ALUMNO NUEVO EN HASSHET*/
    private void btn_alumnos_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alumnos_guardarActionPerformed
        
    Alumno alu = new Alumno();
    
    alu.setNombre(alumnos_input_nombre.getText());
    alu.setApellido(alumnos_input_apellido.getText());
    alu.setNroLegajo(Integer.parseInt(alumnos_input_legajo.getText()));
    
    if (alumno.add(alu)){
    JOptionPane.showMessageDialog(this, "El alumno: " + alu.getNombre() + " " + alu.getApellido() + " Se ha a√±adido correctamente.");
    
    } else {
        JOptionPane.showMessageDialog(this, "El alumno ya existe.");
    }
     
    limpiarFormulario();
   
    mostrarAlumnos();
    }//GEN-LAST:event_btn_alumnos_guardarActionPerformed

   public void mostrarAlumnos() {
    // Suponiendo que 'alumno' es el HashSet de tipo Alumno
    for (Alumno alu : alumno) {
        System.out.println("Nombre: " + alu.getNombre() + ", Apellido: " + alu.getApellido() + ", Legajo: " + alu.getNroLegajo());
    }
}
    
